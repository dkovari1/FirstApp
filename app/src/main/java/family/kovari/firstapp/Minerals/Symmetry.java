/*
*
* Because one of the attributes has only be limited variations (and a few), we can put it in an Enumeration
*
* Every element can be represented with an integer from zero, just like an array(List)
*
* in our example the Minerals symmetry can be regular or monoclic
 */
public enum Symmetry {
    REGULAR, MONOCLOC
}
